# None es la representación de la ausencia de valor
print(None)
# Todos los items tienen una representación booleana (True/false)
print(bool(1))  # True ya que hay valor
print(bool(0))  # False, no hay valor
print(bool(""))  # False, no hay nada
print(bool(" "))  # True, hay un valor
print(bool(0.0))  # Falso, no hay valor